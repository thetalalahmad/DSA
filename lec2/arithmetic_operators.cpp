//  (int/int) = int (Floor value of answer)
#include <iostream>
using namespace std;
int main()
{
    int a = 5 / 2, b = 3 / 5, c = 9 / 2;
    cout << a << endl;
    cout << b << endl;
    cout << c << endl;
    // int/float or float/int = float
    float d = 2.0 / 5; // float/int
    cout << d << endl;

    return 0;
}